#!/bin/bash

colors=("196" "202" "208" "214" "220" "226" "190" "154" "118" "82" "46" "51" "45" "21" "93" "201" "200" "198")

logo=("                  :=**#*+=-.                " \
"               =%@@%##%%%@@@%*=.           " \
"            .+@@@@*###%%#%%%@@@@+-:-:.     " \
"           .%@@@@+::#%@@%%@@@@@@@@@%%%#+-. " \
"           =@@@@@++*%@@@@@@@@@%%%%%%%%%%*+." \
"           ++*%@@@@@@@@+. =***##**=---::.  " \
"          .++++%@%%%%+.    ..:=+=          " \
"          -+++++%%%#*=:::...:=-.           " \
"         -+++++++##**++==-=*#*             " \
"       :++=========+++++++++*#=            " \
"      :%*==============-:.  :-+=           " \
"     .#%*++======--::...     .:--          " \
"     #%#++++=-::........       .:-.        " \
"    *%%*++=--:.........           :        " \
"   +@%#=-------:......          ..:=       " \
" .#@@%=----------:...     ...::::--++      " \
" *@@@@+==========--:...::::::::----=+=     " \
" @@@@@+============++-:::::::-------+*=    " \
":@@@@@#==========+++++=-:::---------+##    " \
"=@@@@@@========++++++++=::----------=%%:   ")

rows=${#logo[@]}
cols=${#logo[0]}

for ((i = 0; i < rows; i++)); do
  for ((j = 0; j < cols; j++)); do
    char="${logo[$i]:$j:1}"
    color_index=$((i + j))
    color="${colors[(color_index) % ${#colors[@]}]}"
    echo -ne "\e[38;5;${color}m${char}"
  done
  echo -e "\e[0m"
done
